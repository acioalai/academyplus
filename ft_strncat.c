#include "header.h"

char    *ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i,j;
	
	i = 0;	
	j = strlen(s1);
	while (n > 0)
	{
		s1[j+i] = s2[i];
		n--;
		i++;
	}
	s1[j+i] = '\0';
	return (s1);
}
