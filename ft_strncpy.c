#include "header.h"

char    *ft_strncpy(char *str1, char *str2, size_t n)
{
	int     i;

	i = 0;
	while ((str2[i] != '\0') && (i < n))
	{
		str1[i] = str2[i];
		i++;
	}
	str1[i] = '\0';
	return(str1);
}
